earth_acceleration = 9.8;

function cm ( x )
{
	return x / 100.0;
}

function mm ( x )
{
	return x / 1000.0;
}

function MPa ( x )
{
	return x * 1e6;
}

function GPa ( x )
{
	return x * 1e9;
}

function grams ( x )
{
	return x / 1000.0;
}

